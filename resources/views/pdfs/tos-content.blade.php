<div id="agb">
    <h1>Allgemeine Bestimmungen der Studierendenschaft HsKA zur Vermietung von Gegenständen</h1>

    <table>
        <tr>
            <td>
                <h2>Allgemeines</h2>
                <ol>
                    <li>Die Studierendenschaft HsKA (nachfolgend Studierendenschaft) stellt einige ihrer Gegenstände und Geräte zur Miete zur Verfügung. Voraussetzung für die Miete ist die Einhaltung dieser Bestimmungen.</li>
                    <li>Zur Miete sind alle an Karlsruher Hochschulen immatrikulierte Studierende berechtigt. Die Sitzung des Allgemeinen Studierendenausschusses kann auf Anfrage die Miete anderen Personen oder Organisationen, insbesondere, wenn diese im studentischen Umfeld angesiedelt sind, gewähren. Näheres wird in Anhang B geregelt.</li>
                    <li>Eine Vermietung kann in Einzelfällen mit angemessener Begründung verweigert werden.</li>
                    <li>Dem Mieter ist eine Weitergabe der Mietgegenstände untersagt. Dies schließt unter anderem Verleih, Untervermietung und Verpfändung ein.</li>
                </ol>

                <h2>Vermietung</h2>
                <ol>
                    <li>Die Mietgegenstände werden von der Studierendenschaft gelagert, gewartet und auf der Grundlage eines Mietvertrags vermietet.</li>
                    <li>Eine Mietanfrage wird über das Online-Buchungsportal auf der Webseite des Allgemeinen Studierendenausschusses gestellt. Dabei werden die Daten des Mieters in der Datenbank der Studierendenschaft aufgenommen.</li>
                    <li>Bei der Mietanfrage ist der vorgesehene Einsatzzweck anzugeben. Insbesondere muss sich bei Veranstaltungen unmissverständlich erkennen lassen, ob ein gewerblicher Charakter vorliegt. Dieser liegt grundsätzlich vor, sobald Eintritt erhoben wird.</li>
                    <li>Der Mieter erhält nach Bearbeitung der Anfrage eine Auftragsbestätigung. Erst mit dieser Bestätigung sind die Gegenstände für den Mieter reserviert.</li>
                    <li>Für die Miete muss eine Kaution bei der Studierendenschaft hinterlegt werden. Die Höhe der Kaution hängt von Wert und Menge der Mietgegenstände ab und wird dem Mieter bei Auftragsbestätigung mitgeteilt.</li>
                    <li>Der Mieter muss bei Abholung ein gültiges Ausweisdokument vorlegen.</li>
                </ol>

                <h2>Mietzeitraum und Fristen</h2>
                <ol>
                    <li>Der Mietzeitraum entspricht dem im Online-Buchungsportal angegebenen Zeitraum. In diesem Zeitraum sind die Mietgegenstände für den Mieter reserviert.</li>
                    <li>Im Einzelfall sowie bei gegebener Verfügbarkeit kann eine frühere Abholung sowie spätere Rückgabe mit dem bearbeitenden Mitarbeiter der Studierendenschaft vereinbart werden. Dies hat keinen Einfluss auf den Mietzins.</li>
                    <li>Eine Verlängerung des Mietzeitraums bedarf der Schriftform.</li>
                    <li>Mietanfragen müssen spätestens eine Woche vor dem Beginn des Mietzeitraums über das Buchungsportal gestellt worden sein. Spätere Reservierungen können evtl. nicht berücksichtigt werden.</li>
                </ol>

                <h2>Entgelt</h2>
                <ol>
                    <li>Die Höhe des Mietzinses errechnet sich aus den im Online-Buchungsportal einsehbaren Tagessätzen der einzelnen Mietgegenstände sowie aus der Länge des Mietzeitraums.</li>
                    <li>Erstreckt sich der Mietzeitraum über mehrere Tage, so wird der letzte Tag nicht für die Berechnung des Mietzinses hergezogen.</li>
                    <li>Bei Vermietungen über mehrere Tage entspricht der Tagessatz für jeden weiteren Tag dem halben Einzeltagessatz.</li>
                    <li>Zusätzlich zum Mietzins ist für den Verbrauch von Betriebsstoffen, wie etwa Nebelfluid, ein Entgelt zu entrichten. Die Verbrauchspreise sind im Online-Buchungsportal einsehbar.</li>
                    <li>Eine unterbliebene oder verspätete Rückgabe, die nicht durch die Studierendenschaft zu verantworten ist, führt zum Rückgabeverzug. Für jeden angefangenen Tag, den sich der Mieter in Verzug befindet, wird der volle Tagessatz fällig.</li>
                    <li>Ist aufgrund der Mietanfrage des Mieters eine konkurrierende Anfrage Dritter abgelehnt worden und storniert der Mieter seine Mietanfrage, nachdem diese bereits bestätigt worden ist, so hat er den dadurch entstandenen Mietausfall zu erstatten.</li>
                    <li>Bei einer Miete, welche keinen gewerblichen Charakter nach § 2 Absatz 3 aufweist, wird Studierenden der Hochschule Karlsruhe auf ein Entgelt gemäß Absätzen 1 bis 3
                        <ol>
                            <li>bei einem Gesamtbetrag von bis zu 30 € eine Rabattierung in Höhe von 100 %,</li>
                            <li>bei einem Gesamtbetrag von bis zu 300 € eine Rabattierung in Höhe von 50 % gewährt.</li>
                        </ol>
                        <p>Zur Ermittlung einer etwaigen Rabattierung wird der Gesamtbetrag nach Berücksichtigung des Mietzeitraums herangezogen. Studierende müssen zur Berücksichtigung der Rabattierung bereits bei Anfragestellung eindeutig als solche erkennbar sein, etwa durch Angabe der Hochschul-E-Mail-Adresse im Online-Portal, sowie bei Abholung einen Studierendenausweis vorzeigen.</p>
                    </li>
                    <li>Im Rahmen von Kooperationsvereinbarungen können etwa studentische Organisationen ganz oder teilweise von der Entrichtung eines Entgelts nach Absatz 1 bis 3 befreit werden. Näheres wird in Anhang B geregelt.</li>
                </ol>
            </td>
            <td>
                <h2>Rückgabe</h2>
                <ol>
                    <li>Die Mietgegenstände sind pünktlich, vollständig, sauber und in einwandfreiem Zustand fristgemäß an die Studierendenschaft zurückzugeben. Umfasst von der Rückgabepflicht sind auch defekte Mietgegenstände und andere Zubehörteile.</li>
                    <li>Bei Rückgabe werden die Mietgegenstände durch die Studierendenschaft in Gegenwart des Mieters unverzüglich auf Vollständigkeit und sichtbare Mängel geprüft. Die erfolgte Rückgabe sowie etwaige Mängel werden schriftlich dokumentiert und durch beide quittiert. Erst mit Dokumentation ist die Vermietung abgeschlossen.</li>
                    <li>Die Studierendenschaft kann die Rücknahme von verschmutzten oder reparaturbedürftigen Mietgegenständen verweigern. In diesem Fall ist eine angemessene Frist zur Nachbesserung zu setzen, welche grundsätzlich zwei Wochen nicht überschreitet. Eine Fristverlängerung kann auf Antrag durch die Sitzung des Allgemeinen Studierendenausschusses genehmigt werden.</li>
                    <li>Entsteht aufgrund einer Nachbesserung ein Mietausfall einer darauffolgenden Vermietung, ist dieser vom Mieter zu erstatten. Der Mietausfall kann mit der Kaution verrechnet werden.</li>
                    <li>Kommt der Mieter der Nachbesserung gemäß Absatz 3 nicht fristgemäß nach, hat er den Wiederbeschaffungswert der betroffenen Mietgegenstände zu erstatten. Die Kaution kann mit diesem verrechnet werden.</li>
                    <li>Alternativ zu Absatz 3 kann eine Mängelbehebung durch die Studierendenschaft auf Kosten des Mieters vorgenommen werden. Entsprechende Kosten können mit der Kaution verrechnet werden.</li>
                </ol>

                <h2>Sorgfaltspflicht des Mieters</h2>
                <ol>
                    <li>Ein Verstoß gegen diese Bestimmungen kann zum zeitweisen oder dauerhaften Ausschluss aus dem Mietangebot der Studierendenschaft führen. Darüber entscheidet die Sitzung des Allgemeinen Studierendenausschusses.</li>
                    <li>Der Mieter verpflichtet sich, die gemieteten Gegenstände ordnungsgemäß zu behandeln und sichert der Studierendenschaft zu, die Gegenstände in einem einwandfreien Zustand zurückzugeben.</li>
                    <li>Technische Mietgegenstände dürfen nur im Rahmen ihrer technischen Bestimmungen und ausschließlich von fachkundigen Personen transportiert, aufgestellt, bedient und abgebaut werden. Der Mieter hat für die fortwährende Einhaltung aller geltenden Sicherheitsrichtlinien, insbesondere der Unfallverhütungsvorschriften (UVV) und der Richtlinien des Verbandes deutscher Elektroingenieure (VDE), zu sorgen.</li>
                    <li>Der Mieter verpflichtet sich, über den beabsichtigten Verwendungszweck genauestens und wahrheitsgemäß Auskunft zu geben und die Mietgegenstände vor jeglichen Zugriffen Dritter zu schützen.</li>
                    <li>Es ist dem Mieter nicht gestattet, eigenmächtige Eingriffe an den Mietgegenständen, wie Veränderungen an der Hard- oder Software, vorzunehmen. Die Lizenzbestimmungen der Softwareanbieter sind einzuhalten. Bei Zuwiderhandlung trägt der Mieter etwaige Reparatur- oder Ersatzbeschaffungskosten in voller Höhe. Diese können mit der Kaution verrechnet werden.</li>
                    <li>Abweichend von Absatz 5 können nach Rücksprache mit der Studierendenschaft Softwareaktualisierungen durch den Mieter vorgenommen werden.</li>
                </ol>

                <h2>Haftung</h2>
                <ol>
                    <li>Bei Übernahme der Mietgegenstände sind ihre Vollständigkeit, der Zustand und die ordnungsgemäße Funktionstüchtigkeit vom Mieter zu überprüfen. Vorhandene Mängel oder fehlende Mietgegenstände sind unverzüglich anzuzeigen. Sofern ein Mangel erst nach Übernahme festgestellt wird oder ein Schaden auftritt, ist dieser ebenfalls unverzüglich schriftlich der Studierendenschaft zu melden.</li>
                    <li>Der Mieter bestätigt durch Quittieren der Übernahme der Mietgegenstände den einwandfreien und zum vertragsmäßigen Gebrauch geeigneten Zustand der Mietgegenstände.</li>
                    <li>Der Mieter haftet für alle Sach- und Personenschäden einschließlich etwaiger Folgeschäden, die durch ihn, seine Beauftragten und sonstige Dritte (z. B. Erfüllungsgehilfen) schuldhaft verursacht wurden; bei Veranstaltungen einschließlich solcher, die durch Besucher verursacht worden sind, sofern der Mieter dazu schuldhaft beigetragen hat oder entsprechende Schäden vorhersehen konnte und zumutbare Schutzmaßnahmen schuldhaft unterlassen hat.</li>
                    <li>Soweit ein Schaden im Verantwortungsbereich des Mieters entstanden ist, obliegt es ihm, fehlendes Verschulden nachzuweisen.</li>
                    <li>Die Studierendenschaft haftet für Schäden des Mieters unbeschränkt nur, sofern diese auf vorsätzliches oder grob fahrlässiges Verhalten der Studierendenschaft zurückzuführen sind. Bei leicht fahrlässigen Pflichtverletzungen haftet die Studierendenschaft nur bei einer Verletzung einer ihrer wesentlichen Vertragspflichten (Kardinalpflicht). In diesem Fall ist die Haftung der Studierendenschaft auf den vertragstypischen und bei Vertragsschluss vorhersehbaren, unmittelbaren Schaden des Mieters beschränkt. Dies gilt auch für Pflichtverletzungen durch gesetzliche Vertreter sowie Erfüllungsgehilfen der Studierendenschaft. Die Haftung wegen schuldhafter Verletzung des Lebens, des Körpers oder der Gesundheit bleibt von dieser Haftungsbeschränkung unberührt.</li>
                </ol>

                <h2>Inkrafttreten</h2>
                <p>Diese Bestimmungen treten mit Beschluss des Allgemeinen Studierendenausschusses vom 24.03.2025 in Kraft und ersetzen alle vorherigen die Vermietung und den Verleih betreffenden Regelungen.</p>
            </td>
